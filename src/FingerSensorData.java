
public class FingerSensorData {
    private int t;
    private int i;
    private int m;
    private int r;
    private int l;

    public FingerSensorData(int t, int i, int m, int r, int l) {
        this.t = t;
        this.i = i;
        this.m = m;
        this.r = r;
        this.l = l;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getM() {
        return m;
    }

    public void setM(int m) {
        this.m = m;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getL() {
        return l;
    }

    public void setL(int l) {
        this.l = l;
    }
}
